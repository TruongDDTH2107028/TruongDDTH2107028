package baitap;
public class whileloop {
    public static void main (String [] args) {
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }
        int y = 0;
        do {
            System.out.println(y);
            y++;
        }
        while (y < 5);
    }
}
